#!/usr/bin/env python3
"""Render assets/data/research.json into the research-program bands in research.html.

The program is shown as a bold question banner followed by one colored lane per
thread, each holding its papers as chips (plus an 'N in progress' chip). Plain
HTML/CSS, no SVG, no JavaScript. Single source of truth: research.json.

Run:  uv run bin/build_network.py
"""
import json
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DATA = ROOT / "assets" / "data" / "research.json"
PAGE = ROOT / "research.html"
MARKER = "<!-- {tag} network (generated by bin/build_network.py) -->"


def esc(s: str) -> str:
    return (s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
             .replace('"', "&quot;"))


def hex_a(hex_color: str, alpha: float) -> str:
    n = int(hex_color.lstrip("#"), 16)
    return f"rgba({n >> 16 & 255},{n >> 8 & 255},{n & 255},{alpha})"


def chip(p: dict, color: str) -> str:
    inner = (f'<b>{esc(p["title"])}</b>'
             f'<span>{esc(p.get("meta", ""))}</span>')
    style = f'background:{hex_a(color, 0.10)};border-left-color:{color}'
    if p.get("href"):
        return (f'<a class="rp-chip" href="{esc(p["href"])}" style="{style}" '
                f'target="_blank" rel="noopener">{inner}</a>')
    return f'<div class="rp-chip" style="{style}">{inner}</div>'


def band(t: dict) -> str:
    chips = "".join(chip(p, t["color"]) for p in t.get("papers", []))
    n = t.get("in_progress", 0)
    if n:
        label = f'{n} in progress' if n > 1 else '1 in progress'
        chips += f'<div class="rp-chip prog">{label}</div>'
    if not chips:
        chips = '<div class="rp-chip prog">early</div>'
    return (
        f'    <div class="rp-band">\n'
        f'      <div class="rp-lbl">\n'
        f'        <span class="rp-num" style="color:{t["color"]}">{esc(t["num"])}</span>\n'
        f'        <span class="rp-nm">{esc(t["name"])}</span>\n'
        f'      </div>\n'
        f'      <div class="rp-chips">{chips}</div>\n'
        f'    </div>'
    )


def build_html(data: dict) -> str:
    bands = "\n".join(band(t) for t in data["threads"])
    return (
        f'  <div class="rp-bands">\n'
        f'    <div class="rp-banner">{esc(data["question"])}</div>\n'
        f'{bands}\n'
        f'  </div>'
    )


def inject(page_text: str, html: str) -> str:
    begin, end = MARKER.format(tag="BEGIN"), MARKER.format(tag="END")
    block = f"{begin}\n{html}\n  {end}"
    pat = re.compile(re.escape(begin) + r".*?" + re.escape(end), re.DOTALL)
    if not pat.search(page_text):
        raise SystemExit("markers not found in research.html")
    return pat.sub(block, page_text)


def main():
    data = json.loads(DATA.read_text())
    PAGE.write_text(inject(PAGE.read_text(), build_html(data)))
    n_pub = sum(len(t.get("papers", [])) for t in data["threads"])
    n_prog = sum(t.get("in_progress", 0) for t in data["threads"])
    print(f"bands: {len(data['threads'])} threads, {n_pub} papers, {n_prog} in progress -> research.html")


if __name__ == "__main__":
    main()
