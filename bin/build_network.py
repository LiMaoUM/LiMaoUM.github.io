#!/usr/bin/env python3
"""Render assets/data/research.json into an inline SVG inside research.html.

The committed SVG is the stable, JS-off base figure. assets/js/network.js
attaches interaction (hover-isolate, drag, click, explore) to the same DOM by
reading the data- attributes written here. Single source of truth: research.json.

Run:  uv run bin/build_network.py
"""
import json
import math
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DATA = ROOT / "assets" / "data" / "research.json"
PAGE = ROOT / "research.html"

R = {"center": 8, "thread": 7, "paper": 6, "progress": 5}
MARKER = "<!-- {tag} network (generated by bin/build_network.py) -->"


def esc(s: str) -> str:
    return (s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
             .replace('"', "&quot;"))


def build_svg(data: dict) -> str:
    vb = " ".join(str(v) for v in data["viewBox"])
    nodes = {n["id"]: n for n in data["nodes"]}

    parts = [
        f'<svg id="network" viewBox="{vb}" role="img" '
        f'aria-label="Network of Mao Li\'s research: one central question branching into five threads and their papers." '
        f'preserveAspectRatio="xMidYMid meet">',
        '<title>One question, five threads</title>',
    ]

    # edges first (under the nodes)
    parts.append('<g class="edges">')
    for e in data["edges"]:
        a, b = nodes[e["from"]], nodes[e["to"]]
        cls = "edge " + e["type"]
        parts.append(
            f'<line class="{cls}" x1="{a["x"]}" y1="{a["y"]}" '
            f'x2="{b["x"]}" y2="{b["y"]}" '
            f'data-from="{e["from"]}" data-to="{e["to"]}" />'
        )
    parts.append('</g>')

    # nodes + labels
    parts.append('<g class="nodes">')
    for n in data["nodes"]:
        kind = n["kind"]
        x, y, r = n["x"], n["y"], R[kind]
        thread = n.get("thread")
        fill = f'var(--{thread})' if thread and kind != "progress" else (
            "var(--ink)" if kind == "center" else "none")
        attrs = [
            f'class="node {kind}"',
            f'data-id="{n["id"]}"',
            f'transform="translate({x} {y})"',
        ]
        if thread:
            attrs.append(f'data-thread="{thread}"')
        if n.get("href"):
            attrs.append(f'data-href="{esc(n["href"])}"')
            attrs.append('tabindex="0" role="link" style="cursor:pointer"')
        parts.append(f'<g {" ".join(attrs)}>')

        if kind == "progress":
            parts.append(
                f'<circle r="{r}" fill="none" stroke="var(--{thread})" '
                f'stroke-width="1.4" stroke-dasharray="2.5 2.5" opacity="0.85" />'
            )
        elif kind == "center":
            parts.append(f'<circle r="{r+3}" fill="none" stroke="var(--ink)" stroke-width="1.4" />')
            parts.append(f'<circle r="{r-3}" fill="var(--ink)" />')
        else:
            parts.append(f'<circle r="{r}" fill="{fill}" stroke="var(--paper)" stroke-width="1.5" />')

        # labels
        W, H = data["viewBox"][2], data["viewBox"][3]
        if kind == "center":
            parts.append(f'<text class="node-label center" text-anchor="middle" y="-{r+18}">{esc(n["label"])}</text>')
            if n.get("sub"):
                parts.append(f'<text class="node-label center" text-anchor="middle" y="-{r+2}">{esc(n["sub"])}</text>')
        elif kind in ("thread", "paper"):
            # anchor labels away from the nearest edge so they never overflow the frame
            if x > W * 0.72:
                anchor, ax = "end", r + 6
            elif x < W * 0.28:
                anchor, ax = "start", -(r + 6)
            else:
                anchor, ax = "middle", 0
            below = y > H * 0.5
            dy = (r + 15) if below else -(r + 9)
            weight = ' style="font-weight:600"' if kind == "thread" else ""
            parts.append(f'<text class="node-label" text-anchor="{anchor}" x="{ax}" y="{dy}"{weight}>{esc(n["label"])}</text>')
            if n.get("sub"):
                dy2 = dy + 13 if below else dy - 13
                parts.append(f'<text class="node-sub" text-anchor="{anchor}" x="{ax}" y="{dy2}">{esc(n["sub"])}</text>')
        parts.append('</g>')
    parts.append('</g>')
    parts.append('</svg>')
    return "\n".join(parts)


def inject(page_text: str, svg: str) -> str:
    begin = MARKER.format(tag="BEGIN")
    end = MARKER.format(tag="END")
    block = f"{begin}\n{svg}\n{end}"
    pat = re.compile(re.escape(begin) + r".*?" + re.escape(end), re.DOTALL)
    if pat.search(page_text):
        return pat.sub(block, page_text)
    raise SystemExit("markers not found in research.html; add BEGIN/END network comments")


def main():
    data = json.loads(DATA.read_text())
    svg = build_svg(data)
    page = PAGE.read_text()
    PAGE.write_text(inject(page, svg))
    n_pub = sum(1 for n in data["nodes"] if n.get("status") in ("published", "preprint"))
    print(f"network: {len(data['nodes'])} nodes ({n_pub} labeled), {len(data['edges'])} edges -> research.html")


if __name__ == "__main__":
    main()
