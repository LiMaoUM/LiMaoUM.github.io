#!/usr/bin/env python3
"""Render _bibliography/papers.bib into papers.html and the home selected list.

Stdlib only (the .bib here is simple and regular, and duplicate keys exist, so
we parse entry blocks and dedupe by title rather than trusting bibtexparser).

Grouping:
  - Lead & first-author work         (Li is first author, non-dataset)
  - Co-authored research articles     (peer-reviewed, non-dataset)
  - Data resources                    (NaNDA / ICPSR archive deposits)

Selected (home page) = entries flagged `selected = {true}`, else the lead work.

Run:  uv run bin/build_papers.py
"""
import re
import html
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
BIB = ROOT / "_bibliography" / "papers.bib"
PAPERS = ROOT / "papers.html"
INDEX = ROOT / "index.html"

VENUE_ABBR = {
    "Social Science Computer Review": "Soc. Sci. Computer Review",
    "Preventive medicine": "Preventive Medicine",
    "Journal of Aging and Health": "J. Aging &amp; Health",
    "Wellbeing, space and society": "Wellbeing, Space &amp; Society",
    "BMC public health": "BMC Public Health",
    "Proceedings of the International AAAI Conference on Web and Social Media (ICWSM)": "Proc. ICWSM",
}


def parse_bib(text):
    entries = []
    for m in re.finditer(r"@(\w+)\s*\{\s*([^,]+),(.*?)\n\}", text, re.DOTALL):
        etype, key, body = m.group(1), m.group(2).strip(), m.group(3)
        fields = {}
        for fm in re.finditer(r"(\w+)\s*=\s*\{(.*?)\}\s*,?\s*(?=\n\s*\w+\s*=|\Z)", body, re.DOTALL):
            fields[fm.group(1).lower()] = re.sub(r"\s+", " ", fm.group(2)).strip()
        fields["_type"] = etype
        fields["_key"] = key
        entries.append(fields)
    return entries


def norm_title(t):
    return re.sub(r"[^a-z0-9]+", " ", t.lower()).strip()


def fmt_authors(raw):
    # bib "Last, First and Last2, First2" OR "First Last and ..."
    people = [a.strip() for a in raw.split(" and ") if a.strip()]
    out = []
    is_me = []
    for p in people:
        if "," in p:
            last, first = [x.strip() for x in p.split(",", 1)]
        else:
            parts = p.split()
            last, first = parts[-1], " ".join(parts[:-1])
        initials = " ".join(w[0] + "." for w in re.split(r"[ \-]", first) if w)
        me = last.lower() == "li" and first.strip().lower() in ("mao", "m", "m.")
        is_me.append(me)
        name = "%s, %s" % (last, initials) if initials else last
        out.append((name, me))
    # collapse long author lists
    frag = []
    for i, (name, me) in enumerate(out):
        sep = "" if i == 0 else (" &amp; " if i == len(out) - 1 else ", ")
        safe = html.escape(name)
        frag.append(sep + ('<span class="me">%s</span>' % safe if me else safe))
    return "".join(frag), (is_me[0] if is_me else False)


def is_dataset(e):
    j = e.get("journal", "").lower()
    t = e.get("title", "").lower()
    return ("national neighborhood data archive" in t or "icpsr" in j
            or "inter-university consortium" in j)


def link_for(e):
    if e.get("arxiv"):
        return '<a href="https://arxiv.org/abs/%s">arXiv</a>' % html.escape(e["arxiv"])
    if e.get("doi"):
        return '<a href="https://doi.org/%s">DOI</a>' % html.escape(e["doi"])
    if e.get("html"):
        url = e["html"]
        m = re.match(r"(https?://)([^/]+)\.proxy\.lib\.umich\.edu(/.*)", url)
        if m:  # proxy rewrites host dots to hyphens; reverse it
            url = m.group(1) + m.group(2).replace("-", ".") + m.group(3)
        return '<a href="%s">Journal</a>' % html.escape(url)
    return ""


def venue_line(e, preprint=False):
    if preprint:
        return "Preprint"
    j = e.get("journal", "").strip()
    j = re.sub(r"\s*\{.*", "", j)  # drop trailing braces junk
    return VENUE_ABBR.get(j, html.escape(j))


def render_entry(e):
    title = html.escape(e.get("title", "").rstrip("."))
    authors, _ = fmt_authors(e.get("author", ""))
    year = e.get("year", "")
    preprint = bool(e.get("arxiv")) or "arxiv" in e.get("journal", "").lower()
    venue = venue_line(e, preprint)
    vclass = "venue preprint" if preprint else "venue"
    link = link_for(e)
    return (
        '    <div class="pub">\n'
        '      <span class="%s">%s · %s</span>\n'
        '      <span class="title">%s.</span>\n'
        '      <span class="authors">%s</span>\n'
        '      %s\n'
        '    </div>'
    ) % (vclass, venue, year,
         title, authors,
         ('<span class="plinks">%s</span>' % link) if link else "")


def block(entries):
    return "\n".join(render_entry(e) for e in entries)


def inject(path, tag, inner):
    begin = "<!-- BEGIN %s (generated by bin/build_papers.py) -->" % tag
    end = "<!-- END %s (generated by bin/build_papers.py) -->" % tag
    txt = path.read_text()
    pat = re.compile(re.escape(begin) + r".*?" + re.escape(end), re.DOTALL)
    if not pat.search(txt):
        raise SystemExit("markers for '%s' not found in %s" % (tag, path.name))
    path.write_text(pat.sub("%s\n%s\n  %s" % (begin, inner, end), txt))


def main():
    raw = parse_bib(BIB.read_text())
    # dedupe by title, prefer the entry with more fields
    seen = {}
    for e in raw:
        k = norm_title(e.get("title", ""))
        if k and (k not in seen or len(e) > len(seen[k])):
            seen[k] = e
    entries = list(seen.values())

    datasets = [e for e in entries if is_dataset(e)]
    scholarly = [e for e in entries if not is_dataset(e)]

    def is_lead(e):
        _, me_first = fmt_authors(e.get("author", ""))
        return me_first
    lead = [e for e in scholarly if is_lead(e)]
    coauth = [e for e in scholarly if not is_lead(e)]

    ykey = lambda e: (-int(e.get("year", "0") or 0), e.get("title", ""))
    lead.sort(key=ykey)
    coauth.sort(key=ykey)
    datasets.sort(key=ykey)

    parts = ['  <div class="pubs">']
    parts.append('    <p class="year-head">Lead &amp; first-author work</p>')
    parts.append(block(lead))
    if coauth:
        parts.append('    <p class="year-head">Co-authored research articles</p>')
        parts.append(block(coauth))
    parts.append('  </div>')
    if datasets:
        parts.append('  <p class="eyebrow" style="margin-top:40px">Data resources</p>')
        parts.append('  <p class="subsection-note">Contributor to the National Neighborhood Data Archive (NaNDA), a set of public ICPSR data deposits.</p>')
        parts.append('  <div class="pubs">')
        parts.append(block(datasets))
        parts.append('  </div>')
    inject(PAPERS, "papers", "\n".join(parts))

    # selected list on home
    selected = [e for e in entries if e.get("selected", "").lower() == "true"] or lead[:3]
    selected.sort(key=ykey)
    inject(INDEX, "selected-papers", '  <div class="pubs" id="selected-papers">\n' + block(selected) + "\n  </div>")

    print("papers: %d lead, %d co-authored, %d data resources; %d selected on home"
          % (len(lead), len(coauth), len(datasets), len(selected)))


if __name__ == "__main__":
    main()
